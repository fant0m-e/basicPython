friends = ["Kevin", "Karen", "Jim", "Oscar", "Toby"] # could be strings, numbers, booleans, any combo.
# friends[1] = "Mike"  --- would modify the list
print(friends)
print(friends[1]) # indexing works like always, starting at 0
print(friends[-1]) # starts indexing from the back with negatives, but starts at 1 in reverse
print(friends[1:]) # element 1 and all elements after
print(friends[1:3]) # range

