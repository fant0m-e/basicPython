from math import * # math is a module

my_num = -11

print(3*(4+5)) # basic arithmetic
print(10 % 3) # modulo (used in true/f or even/odd often)
print(my_num) # prints variables just fine
print(str(my_num) + " is my favorite number")  # converts to string
print(abs(my_num)) # absolute value
print(pow(3, 2)) # 3 to the power of 2
print(max(4, 6)) # gives the largest of the numbers
print(min(4, 6)) # opposite
